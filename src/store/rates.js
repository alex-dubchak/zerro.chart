export function getRate(itemId, date) {
  const timestamp = dayjs(date).valueOf();
  // Return null if the currency doesn't exist in our rates
  if (!rates[itemId]) {
    return null;
  }

  // Check if we have an exact match for the timestamp
  if (rates[itemId][timestamp]) {
    return rates[itemId][timestamp];
  }

  // Find the closest timestamp
  const timestamps = Object.keys(rates[itemId]).map(Number);

  // If no timestamps are available, return null
  if (timestamps.length === 0) {
    return null;
  }

  // Find the latest timestamp available
  const latestTimestamp = Math.max(...timestamps);

  // If requested timestamp is greater than latest available, return null
  if (timestamp > latestTimestamp) {
    return null;
  }

  // Find the closest timestamp (the one with minimum absolute difference)
  let closestTimestamp = timestamps[0];
  let minDifference = Math.abs(timestamp - closestTimestamp);

  for (let i = 1; i < timestamps.length; i++) {
    const difference = Math.abs(timestamp - timestamps[i]);
    if (difference < minDifference) {
      minDifference = difference;
      closestTimestamp = timestamps[i];
    }
  }
  // console.log('Closest timestamp:', dayjs(closestTimestamp).format('YYYY-MM-DD'), 'for', date);
  // Return the rate for the closest timestamp
  return rates[itemId][closestTimestamp];
}